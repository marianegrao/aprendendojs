// area da circunferencia = PI (usa letra maiuscula pois é uma constante)* raio * raio

let raio = 15;
const PI =  3.14;
let areaDaCircunfe = PI * raio * raio; // ou Math.PI * Math.pow(raio, 2)


console.log( "Se PI igual a " + PI + " e raio igual a "+ raio 
             + " metros," + " o valor da área da circunferência será de "
             + areaDaCircunfe + " m2.");


